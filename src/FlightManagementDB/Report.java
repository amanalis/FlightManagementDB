    private void NameKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_NameKeyTyped
        // TODO add your handling code here:
        char c =evt.getKeyChar();
        if((Character.isDigit(c))){
            evt.consume();
        }
    }//GEN-LAST:event_NameKeyTyped

    private void MobileKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_MobileKeyTyped
        // TODO add your handling code here:
        String MobileNo = Mobile.getText();
        int lenght = MobileNo.length();
        
        char c = evt.getKeyChar();
        
        if(evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9'){
            if(lenght <11){
                Mobile.setEditable(true);
            }
            else{
                Mobile.setEditable(false);
            }
        }
        else{
            if(evt.getExtendedKeyCode()==KeyEvent.VK_BACK_SPACE || evt.getExtendedKeyCode()==KeyEvent.VK_DELETE){
                Mobile.setEditable(true);
            }
            else{
                Mobile.setEditable(false);
            }
                
        }
    }//GEN-LAST:event_MobileKeyTyped

    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "Insert into report_problem values (?,?,?,?,?)";
            PreparedStatement st = conn.prepareStatement(sql);
            
            st.setInt(1, Integer.parseInt(RID.getText()));
            st.setString(2, Name.getText());
            st.setString(3, Email.getText());
            st.setInt(4, Integer.parseInt(Mobile.getText()));
            st.setString(5, Problem.getText());
            
            st.executeUpdate();
            JOptionPane.showMessageDialog(null, "Sorry for problem, we'll contact you shortly.");
            new MainMenu().setVisible(true);
            dispose();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_SubmitActionPerformed

